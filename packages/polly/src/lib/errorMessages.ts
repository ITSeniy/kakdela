/**
 * Возвращает дружелюбное сообщение по коду ошибки. Если кода нет в карте —
 * отдаёт `fallback` (серверный текст, который для ряда кодов уже хороший
 * русский с деталями вроде размера файла).
 */
export function friendlyMessage(code: string, fallback: string): string {
  return ERROR_MESSAGES[code] ?? fallback
}
